'use client'

import { useState, useEffect } from 'react'
import { createClient } from '@/lib/supabase/client'
import { Button } from '@/components/ui/button'
import { Alert, AlertDescription } from '@/components/ui/alert'
import { Loader2 } from 'lucide-react'

export function LoginClient() {
  const [isLoading, setIsLoading] = useState(false)
  const [error, setError] = useState<string | null>(null)
  const [urlError, setUrlError] = useState<string | null>(null)
  const [urlMessage, setUrlMessage] = useState<string | null>(null)
  const [debugInfo, setDebugInfo] = useState<string | null>(null)

  // Read URL params on client side to avoid SSR bailout
  useEffect(() => {
    const params = new URLSearchParams(window.location.search)
    setUrlError(params.get('error'))
    setUrlMessage(params.get('message'))
  }, [])

  const handleGoogleSignIn = async () => {
    setIsLoading(true)
    setError(null)
    setDebugInfo(null)

    // Safety timeout: if redirect doesn't happen within 8s, show diagnostic error
    const timeoutId = setTimeout(() => {
      setError('Login gagal â€” browser tidak bisa redirect ke Google. Hubungi admin.')
      setDebugInfo(`Origin: ${window.location.origin} | URL: ${process.env.NEXT_PUBLIC_SUPABASE_URL ? 'set' : 'MISSING'} | Key: ${process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY ? 'set' : 'MISSING'}`)
      setIsLoading(false)
    }, 8000)

    try {
      const supabase = createClient()

      const { data, error } = await supabase.auth.signInWithOAuth({
        provider: 'google',
        options: {
          redirectTo: `${window.location.origin}/auth/callback`,
        },
      })

      if (error) {
        clearTimeout(timeoutId)
        setError(`Login error: ${error.message}`)
        setIsLoading(false)
        return
      }

      // If we have a URL but browser didn't redirect, try manual redirect
      if (data?.url) {
        clearTimeout(timeoutId)
        window.location.href = data.url
      }
    } catch (e) {
      clearTimeout(timeoutId)
      const errMsg = e instanceof Error ? e.message : String(e)
      setError(`Terjadi kesalahan: ${errMsg}`)
      setIsLoading(false)
    }
  }

  const displayError = error || urlError

  return (
    <>
      {displayError && (
        <Alert variant="destructive">
          <AlertDescription>{displayError}</AlertDescription>
        </Alert>
      )}

      {debugInfo && (
        <Alert>
          <AlertDescription className="font-mono text-xs">{debugInfo}</AlertDescription>
        </Alert>
      )}

      {urlMessage && (
        <Alert>
          <AlertDescription>{urlMessage}</AlertDescription>
        </Alert>
      )}

      <Button
        onClick={handleGoogleSignIn}
        disabled={isLoading}
        className="w-full"
        size="lg"
      >
        {isLoading ? (
          <>
            <Loader2 className="mr-2 h-4 w-4 animate-spin" />
            Signing in...
          </>
        ) : (
          <>
            <GoogleIcon className="mr-2 h-4 w-4" />
            Sign in with Google
          </>
        )}
      </Button>
    </>
  )
}

