// Supabase client factories for Edge Functions
// Two clients: user (RLS) and service (bypass RLS for audit writes)

import { createClient } from 'https://esm.sh/@supabase/supabase-js@2'

/**
 * Create a Supabase client that acts as the authenticated user.
 * RLS policies apply — queries scoped to user's permissions.
 */
export function createUserClient(authHeader: string) {
  return createClient(
    Deno.env.get('SUPABASE_URL')!,
    Deno.env.get('SUPABASE_ANON_KEY')!,
    {
      global: {
        headers: { Authorization: authHeader },
      },
    }
  )
}

/**
 * Create a Supabase client with service role key.
 * Bypasses RLS — use only for audit logs and system operations.
 */
export function createServiceClient() {
  return createClient(
    Deno.env.get('SUPABASE_URL')!,
    Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!
  )
}
